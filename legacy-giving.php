<div class="site-width legacy-content">
  <h3>Bequests</h3>
  Gifts made through bequests (leaving assets to a charity in your Will or Living Trusts) are generally deductible for Estate Tax purposes, reducing the potential to pay Estate Tax.<br>
  <br>

  <h3>Charitable Remainder Trusts (CRTs) and Charitable Lead Trusts (CLTs)</h3>
  These trusts can also offer estate tax deductions, as the charitable gift portion of the trust is exempt from estate taxes.<br>
  <br>

  <h3>Life Insurance Policies</h3>
  Designating a charity as a beneficiary of a life insurance policy can also result in estate tax savings.<br>
  <br>

  <h3>IRAs</h3>
  A person may donate up to $108,000 from an IRA directly to a qualified charity without having to pay income taxes. This is commonly known as an IRA charitable rollover or a qualified charitable rollover (QCD). You may also designate a charity as a beneficiary of an IRA (or other retirement plan) to reduce both Income and Estate Tax.<br>
  <br>

  <h3>Donating Appreciated Assets</h3>
  Donating appreciated assets, such as stocks, to a 501c3 public charity can allow you to avoid capital gain taxes on the appreciation.<br>
  <br>

  <h3>Cash Donations</h3>
  Cash donations are generally eligible for an income tax deduction of up to 60% of your adjusted gross income.<br>
  <br>
